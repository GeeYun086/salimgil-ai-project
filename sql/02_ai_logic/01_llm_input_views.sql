    TOTAL_SALES,
    FOOD_SALES,
    HOME_LIFE_SERVICE_SALES,
    ELECTRONICS_FURNITURE_SALES,
    LARGE_DISCOUNT_STORE_SALES,
    SMALL_RETAIL_STORE_SALES,
    E_COMMERCE_SALES
FROM FACT_REGION_MONTH_PROFILE;

// 지역/월 기준으로 계약 수가 높은 렌탈 후보 뷰(미분류 제거)
CREATE OR REPLACE VIEW VW_LLM_RENTAL_CANDIDATES AS
SELECT
    REGION_ID,
    YEAR_MONTH,
    RENTAL_MAIN_CATEGORY,
    RENTAL_SUB_CATEGORY,
    CONTRACT_COUNT,
    AVG_NET_SALES,
    AVG_POLICY_AMOUNT,
    ROW_NUMBER() OVER (
        PARTITION BY REGION_ID, YEAR_MONTH
        ORDER BY CONTRACT_COUNT DESC, AVG_NET_SALES DESC
    ) AS RN
FROM FACT_RENTAL_MARKET_TREND
WHERE COALESCE(RENTAL_MAIN_CATEGORY, '') <> '미분류'
  AND COALESCE(RENTAL_MAIN_CATEGORY, '') <> ''
  AND COALESCE(RENTAL_SUB_CATEGORY, '') <> '미분류'
  AND COALESCE(RENTAL_SUB_CATEGORY, '') <> ''
  AND AVG_NET_SALES IS NOT NULL;

// 확인
SELECT *
FROM VW_LLM_RENTAL_CANDIDATES
WHERE REGION_ID = 1
ORDER BY YEAR_MONTH DESC, RN
LIMIT 20;

