// 체크리스트 라벨 정규화 뷰 만들기
CREATE OR REPLACE VIEW VW_CHECKLIST_RULE_NORMALIZED AS
SELECT
    RULE_ID,
    HOUSEHOLD_SEGMENT AS SOURCE_HOUSEHOLD_SEGMENT,
    CASE
        WHEN HOUSEHOLD_SEGMENT = '육아가구' THEN '육아가구형'
        WHEN HOUSEHOLD_SEGMENT = '일반가구' THEN '일반가족형'
        WHEN HOUSEHOLD_SEGMENT = '프리미엄가구' THEN '일반가족형'
        WHEN HOUSEHOLD_SEGMENT = '가성비가구' THEN '1인가구형'
        ELSE '일반가족형'
    END AS HOUSEHOLD_TYPE,
    BUDGET_TIER,
    ITEM_NAME,
    PRIORITY,
    IS_RENTAL_RELATED,
    ITEM_CATEGORY
FROM DIM_CHECKLIST_RULE;

// 확인
SELECT *
FROM VW_CHECKLIST_RULE_NORMALIZED
ORDER BY HOUSEHOLD_TYPE, BUDGET_TIER, PRIORITY;

// 완전일치 후보만 뽑는 뷰 생성
