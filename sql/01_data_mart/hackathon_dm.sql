//가구유형 분류 표
CREATE OR REPLACE VIEW VW_REGION_HOUSEHOLD_SEGMENT AS
SELECT
    REGION_ID,
    REGION_NAME,
    YEAR_MONTH,
    AVG_INCOME,
    AVG_HOUSEHOLD_INCOME,
    AVG_ASSET_AMOUNT,
    RATE_HIGHEND,
    AVG_MEME_PRICE_PER_SUPPLY_PYEONG,
    AVG_JEONSE_PRICE_PER_SUPPLY_PYEONG,
    AGE_UNDER5,
    FEMALE_20TO40,
    AGE_UNDER5_PER_FEMALE_20TO40,
    CASE
        WHEN AGE_UNDER5_PER_FEMALE_20TO40 >= 0.15 THEN '육아가구'
        WHEN AVG_HOUSEHOLD_INCOME >= 80000 AND AVG_MEME_PRICE_PER_SUPPLY_PYEONG >= 3000 THEN '프리미엄가구'
        WHEN AVG_HOUSEHOLD_INCOME < 50000 THEN '가성비가구'
        ELSE '일반가구'
    END AS HOUSEHOLD_SEGMENT,
    CASE
        WHEN AVG_HOUSEHOLD_INCOME >= 80000 THEN 'PREMIUM'
        WHEN AVG_HOUSEHOLD_INCOME >= 50000 THEN 'MID'
        ELSE 'BASIC'
    END AS BUDGET_TIER
FROM FACT_REGION_MONTH_PROFILE;

//확인
SELECT *
FROM VW_REGION_HOUSEHOLD_SEGMENT
ORDER BY REGION_ID, YEAR_MONTH
LIMIT 20;

