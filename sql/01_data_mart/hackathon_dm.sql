//FACT_REGION_MONTH_PROFILE 만들기
CREATE OR REPLACE TABLE FACT_REGION_MONTH_PROFILE AS
SELECT
    R.REGION_ID,
    R.REGION_NAME,
    C.YEAR_MONTH,
    C.TOTAL_SALES,
    C.TOTAL_COUNT,
    C.FOOD_SALES,
    C.HOME_LIFE_SERVICE_SALES,
    C.EDUCATION_ACADEMY_SALES,
    C.ELECTRONICS_FURNITURE_SALES,
    C.LARGE_DISCOUNT_STORE_SALES,
    C.SMALL_RETAIL_STORE_SALES,
    C.E_COMMERCE_SALES,
    A.CUSTOMER_COUNT,
    A.AVG_INCOME,
    A.AVG_HOUSEHOLD_INCOME,
    A.MEDIAN_INCOME,
    A.AVG_ASSET_AMOUNT,
    A.RATE_HIGHEND,
    A.OWN_HOUSING_COUNT,
    A.MULTIPLE_HOUSING_COUNT,
    P.TOTAL_HOUSEHOLDS,
    P.AVG_MEME_PRICE_PER_SUPPLY_PYEONG,
    P.AVG_JEONSE_PRICE_PER_SUPPLY_PYEONG,
    F.AGE_UNDER5,
    F.FEMALE_20TO40,
    F.AGE_UNDER5_PER_FEMALE_20TO40
FROM DIM_REGION R
JOIN AGG_CARD_SALES_REGION_MONTH C
    ON R.SPH_CITY_CODE = C.CITY_CODE
LEFT JOIN AGG_ASSET_INCOME_REGION_MONTH A
    ON R.SPH_CITY_CODE = A.CITY_CODE
   AND C.YEAR_MONTH = A.YEAR_MONTH
LEFT JOIN AGG_APT_PRICE_REGION_MONTH P
    ON R.RICHGO_SGG = P.SGG
   AND C.YEAR_MONTH = P.YEAR_MONTH
LEFT JOIN AGG_FAMILY_PROFILE_REGION_MONTH F
    ON R.RICHGO_SGG = F.SGG
   AND C.YEAR_MONTH = F.YEAR_MONTH;

