CREATE OR REPLACE VIEW VW_MOVING_RECOMMENDATION AS
SELECT
    S.REGION_ID,
    S.REGION_NAME,
    S.YEAR_MONTH,
    S.HOUSEHOLD_SEGMENT,
    S.BUDGET_TIER,
    C.ITEM_NAME,
    C.PRIORITY,
    C.IS_RENTAL_RELATED,
    C.ITEM_CATEGORY,
    T.RENTAL_MAIN_CATEGORY,
    T.RENTAL_SUB_CATEGORY,
    T.AVG_NET_SALES,
    T.AVG_POLICY_AMOUNT
FROM VW_REGION_HOUSEHOLD_SEGMENT S
LEFT JOIN DIM_CHECKLIST_RULE C
    ON S.HOUSEHOLD_SEGMENT = C.HOUSEHOLD_SEGMENT
   AND S.BUDGET_TIER = C.BUDGET_TIER
LEFT JOIN FACT_RENTAL_MARKET_TREND T
    ON S.REGION_ID = T.REGION_ID
   AND S.YEAR_MONTH = T.YEAR_MONTH;

